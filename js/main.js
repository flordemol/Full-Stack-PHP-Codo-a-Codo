const renderActividades = document.getElementById("listaActividades");
const renderTipoAct = document.getElementById("tipoAct");
const renderHerramientas = document.getElementById("herramienta");

// Mostrar icono en la tarjeta
function iconos(tarea) {
    let iconos = "";

    if(tarea.herramientas.includes('Foro')) 
        iconos += '<i class="fas fa-chalkboard-teacher"></i>'
    if(tarea.herramientas.includes('HTML')) 
        iconos += '<i class="fab fa-html5"></i>'
    if(tarea.herramientas.includes('CSS')) 
        iconos += '<i class="fab fa-css3-alt"></i>'
    if(tarea.herramientas.includes('Bootstrap')) 
        iconos += '<i class="fab fa-bootstrap"></i>'
    if(tarea.herramientas.includes('Javascript')) 
        iconos += '<i class="fab fa-js-square"></i>'
    if(tarea.herramientas.includes('PHP')) 
        iconos += '<i class="fab fa-php"></i>'
    
    return iconos;
}

// Borra todas las tarjetas
function limpiarActividades(){
    renderActividades.innerHTML = "";
}

// Filtros
renderTipoAct.addEventListener("change", chequearFiltros);
renderHerramientas.addEventListener("change", chequearFiltros);

function chequearFiltros(){
    const tipoAct = renderTipoAct.options[renderTipoAct.selectedIndex].text;
    const herr = renderHerramientas.options[renderHerramientas.selectedIndex].text;

    listarActividades(tipoAct, herr)
}

function listarActividades(tipoAct, herr){
    console.log(tipoAct + " " + herr)
    limpiarActividades();
    
    for(let tarea of tareas){
        
        if(tipoAct == "Todo" && herr == "Todo") render(tarea);
        
        if((tarea.tipoActividad == tipoAct) && (tarea.herramientas.includes(herr))) render(tarea);

        if(tipoAct == "Todo" && tarea.herramientas.includes(herr)) render(tarea);
        
        if((tarea.tipoActividad == tipoAct) && herr == "Todo") render(tarea);
    }
}

listarActividades("Todo", "Todo");

// Crear tarjeta
function render(tarea){
    let ico = iconos(tarea);

    renderActividades.innerHTML += `
    <div class="col-lg-4 col-md-6 col-12">
          <a href="${tarea.href}" target="_blank">
            <div class="card">
              <img
                src="imagenes/${tarea.imagen}"
                class="card-img-top"
                alt="${tarea.altIMG}"
              />
              <div class="card-body">
                <p class="card-text">
                  ${tarea.titulo} 
                </p>
                <p class="card-text logos">
                  ${ico}
                </p>
              </div></div
          ></a>
        </div>
    `
}